def _build_contract_yaml(mod: dict) -> str:
    """Build a CONTRACT.yaml string from a module definition."""
    contract = {
        "module": mod["module"],
        "version": 1,
        "conventions_version": 2,
        "status": mod["status"],
        "type": "regular",
        "purpose": mod["purpose"],
        "provides": mod["provides"],
        "consumes": [
            {"module": c["module"], "interface": c["interface"], "required": c["required"], "contract_version": 1}
            for c in mod["consumes"]
        ] if mod["consumes"] else [],
    }

    # Add contract_rules
    if mod["status"] == "frozen":
        contract["contract_rules"] = {
            "adding_interface": "allowed",
            "modifying_interface": "forbidden",
            "removing_interface": "forbidden",
        }
    elif mod["status"] != "draft":
        contract["contract_rules"] = {
            "adding_interface": "allowed",
            "modifying_interface": "notify",
            "removing_interface": "breaking",
        }

    return yaml.dump(contract, default_flow_style=False, sort_keys=False, width=120, allow_unicode=True)


def _build_state_yaml(mod: dict) -> str:
    """Build a STATE.yaml string."""
    state = {
        "module": mod["module"],
        "status": "green" if mod["status"] in ("stable", "frozen") else "yellow",
        "updated": "2026-05-24T00:00:00Z",
        "current_work": mod["state"]["current_work"],
        "blockers": mod["state"]["blockers"],
    }
    return yaml.dump(state, default_flow_style=False, sort_keys=False, width=120)


def _build_memory_yaml(mod: dict) -> str:
    """Build a MEMORY.yaml string."""
    memory = {
        "module": mod["module"],
        "entries": [
            {"type": e["type"], "content": e["content"]}
            for e in mod.get("memory", [])
        ]
    }
    return yaml.dump(memory, default_flow_style=False, sort_keys=False, width=120)


def _build_assumptions_yaml(mod: dict) -> str:
    """Build a minimal ASSUMPTIONS.yaml."""
    assumptions = {
        "module": mod["module"],
        "assumptions": [],
    }
    return yaml.dump(assumptions, default_flow_style=False, sort_keys=False, width=120)


def _build_manifest(archetype: dict) -> str:
    """Build MANIFEST.yaml for the project."""
    manifest = {
        "project": archetype["name"],
        "managers": {}
    }
    for manager, modules in archetype["managers"].items():
        manifest["managers"][manager] = {
            "modules": modules
        }
    return yaml.dump(manifest, default_flow_style=False, sort_keys=False, width=120)


def generate_archetype(archetype: dict, output_dir: Path):
    """Generate a full project directory for one archetype."""
    project_dir = output_dir / archetype["name"]
    project_dir.mkdir(parents=True, exist_ok=True)

    # Write MANIFEST.yaml
    (project_dir / "MANIFEST.yaml").write_text(_build_manifest(archetype))

    # Write modules
    for mod in archetype["modules"]:
        mod_dir = project_dir / "modules" / mod["module"]
        mod_dir.mkdir(parents=True, exist_ok=True)

        (mod_dir / "CONTRACT.yaml").write_text(_build_contract_yaml(mod))
        (mod_dir / "STATE.yaml").write_text(_build_state_yaml(mod))
        (mod_dir / "MEMORY.yaml").write_text(_build_memory_yaml(mod))
        (mod_dir / "ASSUMPTIONS.yaml").write_text(_build_assumptions_yaml(mod))

    print(f"  ✓ {archetype['name']}: {len(archetype['modules'])} modules")


def main():
    parser = argparse.ArgumentParser(description="Generate ANMA benchmark archetypes")
    parser.add_argument("--output-dir", default="benchmark_projects",
                        help="Directory to write archetype projects")
    args = parser.parse_args()

    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    print(f"Generating {len(ARCHETYPES)} project archetypes...\n")

    for archetype in ARCHETYPES:
        generate_archetype(archetype, output_dir)

    print(f"\nDone. {len(ARCHETYPES)} projects generated in {output_dir}/")
    print(f"Total modules: {sum(len(a['modules']) for a in ARCHETYPES)}")

