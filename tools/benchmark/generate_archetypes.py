    index = []
    for a in ARCHETYPES:
        index.append({
            "name": a["name"],
            "description": a["description"],
            "module_count": len(a["modules"]),
            "manager_count": len(a["managers"]),
        })
    index_path = output_dir / "archetype_index.yaml"
    index_path.write_text(yaml.dump(index, default_flow_style=False, sort_keys=False))
    print(f"Index written to {index_path}")


